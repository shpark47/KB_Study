package variable;

public class Primitive {
    public static void main(String[] args) {
        // 기본형 4가지
        // 정수, 실수, 문자1, 논리
        int age = 100; // 정적타이핑
        // 실수
        double height = 122.2; // 소수점 15자리까지
        // 문자1
        char gender = '남';
        // 논리
        boolean food = true;

        long money = 333333333333333L;
        float weight = 88.8f;
        String name = "홍길동";

        System.out.println("나이는 : " + age + "세");
        System.out.println("성별은 : " + gender + "임");
    }
}
